// while loop example
let i = 1;
while (i <= 5) {
    console.log("While loop count:", i);
    i++;
}

// for loop example
for (let i = 1; i <= 5; i++) {
    console.log("For loop count:", i);
}

// do while loop example
let j = 1;
do {
    console.log("Do while loop count:", j);
    j++;
} while (j <= 5);
