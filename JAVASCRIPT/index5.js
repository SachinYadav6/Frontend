// unary operator
let age = 23;
age = age + 3;
console.log(age);
age -= 6;
console.log(age);

// pre increment (change ,then use)
let pencilcost = 12;
++pencilcost;
console.log(pencilcost);
--pencilcost;
console.log(pencilcost);


//  post increment 
let num = 5;
num++;
console.log(num);
num--;
console.log(num);
